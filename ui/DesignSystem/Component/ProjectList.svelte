<script>
  import { Text, Button } from "../Primitive";

  import ProjectCard from "./ProjectCard.svelte";

  import { projectNameStore } from "../../store/project.js";
  import { createProject, projectSource } from "../../lib/path.js";

  import { gql } from "apollo-boost";
  import { getClient, query } from "svelte-apollo";
  import { push } from "svelte-spa-router";

  const GET_PROJECTS = gql`
    query Query {
      projects {
        id
        metadata {
          defaultBranch
          imgUrl
          description
          name
        }
        registered {
          ... on OrgRegistration {
            orgId
          }
          ... on UserRegistration {
            userId
          }
        }
        stats {
          branches
          commits
          contributors
        }
      }
    }
  `;

  const client = getClient();
  const projects = query(client, { query: GET_PROJECTS });
  projects.refetch();
</script>

<style>
  ul {
    min-width: 500px;
  }

  li {
    display: flex;
    width: 100%;
    height: 96px;
    flex: 1;
    border-bottom: 1px solid var(--color-lightgray);
    cursor: pointer;
    padding: 22px 15px 26px 12px;
  }

  li:hover {
    background-color: var(--color-almostwhite);
  }

  li:last-child {
    border-bottom: 0;
  }

  .wrapper {
    margin-top: 156px;
    display: flex;
    justify-content: center;
  }

  .create-project {
    text-align: center;
    width: 240px;
  }
</style>

{#await $projects}
  <Text>Loading projects...</Text>
{:then result}
  {#if result.data.projects.length > 0}
    <ul>
      {#each result.data.projects as project}
        <li
          on:click={() => {
            projectNameStore.set(project.metadata.name);
            push(projectSource(project.id));
          }}
          class="project-card">
          <ProjectCard
            projectId={project.id}
            title={project.metadata.name}
            description={project.metadata.description}
            isRegistered={project.registered}
            commitCount={project.stats.commits}
            branchCount={project.stats.branches}
            memberCount={project.stats.contributors} />
        </li>
      {/each}
    </ul>
  {:else}
    <div class="wrapper">
      <div class="create-project">
        <Text
          variant="title"
          style="color: var(--color-darkgray); margin-bottom: 13px">
          You have no projects
        </Text>
        <Text style="color: var(--color-gray)">
          Create a new project and share it with friends to get started
        </Text>
        <Button
          style="margin: 23px auto"
          variant="primary"
          on:click={() => {
            push(createProject());
          }}>
          Create a new project
        </Button>
      </div>
    </div>
  {/if}
{:catch error}
  <p>ERROR: {error}</p>
{/await}
